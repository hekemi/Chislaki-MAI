"""
Метод Ньютона (Метод касательных) для нахождения корня уравнения f(x) = 0.

Формула итерации:
    x_{n+1} = x_n - f(x_n) / f'(x_n)

Функция newton() принимает:
    f       - функция, корень которой ищем
    x0      - начальное приближение
    df      - производная f (необязательно; если не передана, вычисляется численно)
    tol     - требуемая точность (по умолчанию 1e-7)
    max_iter - максимальное число итераций (по умолчанию 1000)

Возвращает найденное значение корня (float).
Генерирует ValueError, если метод не сошёлся за max_iter итераций.
"""


def newton(f, x0, df=None, tol=1e-7, max_iter=1000, diff_step=1e-7):
    """Найти корень уравнения f(x)=0 методом Ньютона.

    Параметры
    ----------
    f : callable
        Функция, корень которой ищем.
    x0 : float
        Начальное приближение.
    df : callable, optional
        Производная функции f. Если не передана, вычисляется численно
        методом центральных разностей.
    tol : float
        Допустимая погрешность (критерий останова |x_{n+1} - x_n| < tol).
    max_iter : int
        Максимальное число итераций.
    diff_step : float
        Шаг для численного дифференцирования методом центральных разностей
        (используется только когда df не передан). Значение ~sqrt(машинного
        эпсилона) обеспечивает баланс между погрешностью округления и
        погрешностью дискретизации.

    Возвращает
    ----------
    float
        Приближённое значение корня уравнения f(x) = 0.

    Исключения
    ----------
    ValueError
        Если производная в текущей точке близка к нулю или метод не сошёлся
        за max_iter итераций.
    """
    x = float(x0)
    for i in range(max_iter):
        fx = f(x)

        # Вычисляем производную: аналитически или численно
        if df is not None:
            dfx = df(x)
        else:
            dfx = (f(x + diff_step) - f(x - diff_step)) / (2 * diff_step)

        if abs(dfx) < 1e-12:
            raise ValueError(
                f"Производная близка к нулю в точке x={x} на итерации {i}. "
                "Метод Ньютона не применим."
            )

        x_new = x - fx / dfx

        if abs(x_new - x) < tol:
            return x_new

        x = x_new

    raise ValueError(
        f"Метод Ньютона не сошёлся за {max_iter} итераций. "
        f"Последнее приближение: x={x}"
    )
