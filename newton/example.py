"""Примеры использования метода Ньютона из newton.py."""

import math
from newton import newton

# ─────────────────────────────────────────────
# Пример 1: f(x) = x^2 - 2  =>  корень: sqrt(2)
# ─────────────────────────────────────────────
f1 = lambda x: x**2 - 2
df1 = lambda x: 2 * x
root1 = newton(f1, x0=1.0, df=df1)
print(f"Пример 1: корень f(x)=x²-2  →  x = {root1:.10f}  (ожидается {math.sqrt(2):.10f})")

# ─────────────────────────────────────────────
# Пример 2: f(x) = cos(x) - x  =>  корень ~0.7391
# Производная не передаётся — вычисляется численно
# ─────────────────────────────────────────────
f2 = lambda x: math.cos(x) - x
root2 = newton(f2, x0=0.5)
print(f"Пример 2: корень f(x)=cos(x)-x  →  x = {root2:.10f}")

# ─────────────────────────────────────────────
# Пример 3: f(x) = x^3 - x - 2  =>  корень ~1.5214
# ─────────────────────────────────────────────
f3 = lambda x: x**3 - x - 2
df3 = lambda x: 3 * x**2 - 1
root3 = newton(f3, x0=1.5, df=df3)
print(f"Пример 3: корень f(x)=x³-x-2  →  x = {root3:.10f}")
